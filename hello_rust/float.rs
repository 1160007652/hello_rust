
// 学习 浮点类型 的知识

fn main(){

    let _: f32 = 1.12345;        // f32 类型声明
    let _      = 2.4345f32;      // 类型后缀声明
    let _      = 3.4567;         // 默认 f64 类型声明
    let _      = 123_45.623_1;         // _分隔符，增强可读性

}

/*
 *
 * 浮点类型：分为 f32 、f64 两大类型
 * 
 * f32有效精度：小数点后保留 6 位有效数字
 * 
 * f64有效精度：小数点后保留 15 位有效数字
 *
 */